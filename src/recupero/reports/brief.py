# Single source of truth for the freeze_brief.json schema version.
# Imported by emit_brief.py + the worker synthesizer so a future bump
# only happens here.
BRIEF_SCHEMA_VERSION = "0.16.4"

# Earliest version that wrote ALL the fields the current rendering
# chain expects (evidence_type, evidence_mode, etc.). Briefs from
# before this version trigger a stale-brief warning so operators
# know the rendering may use "currently held" language for what
# should be historical-receipt cases.
_MIN_RECOGNIZED_BRIEF_VERSION = "0.14.8"


def check_brief_schema_version(brief: dict[str, Any]) -> str | None:
    """Inspect brief.SCHEMA_VERSION. Returns None if the brief is
    current; returns a human-readable warning string if it's stale
    or missing the version stamp entirely.

    Callers should log the warning and surface it in the rendered
    output (banner, header note, etc.). The reader does NOT refuse
    to render — the brief may still be partially useful — but the
    caller now has the data to be honest about its provenance.
    """
    version = brief.get("SCHEMA_VERSION")
    # Warning strings use ASCII dashes (not em-dashes) so they render
    # correctly on Windows cp1252 stdout where operator logs are read.
    if version is None:
        return (
            f"Brief lacks SCHEMA_VERSION stamp -- generated by a pre-"
            f"v{BRIEF_SCHEMA_VERSION} pipeline. Some evidence-type / "
            "evidence_mode fields may be absent, causing the rendering "
            "to fall back to 'currently held' language even for "
            "historical-receipt cases. Re-emit the brief by running "
            "`recupero emit-brief <case_id>` against the latest "
            f"freeze_asks.json to get full v{_MIN_RECOGNIZED_BRIEF_VERSION}+ "
            "rendering."
        )
    # Both version strings are dot-separated "MAJOR.MINOR.PATCH". A
    # simple tuple comparison handles all cases we care about.
    def _as_tuple(s: str) -> tuple[int, ...]:
        try:
            return tuple(int(p) for p in s.split("."))
        except Exception:  # noqa: BLE001
            return (0, 0, 0)
    if _as_tuple(version) < _as_tuple(_MIN_RECOGNIZED_BRIEF_VERSION):
        return (
            f"Brief was generated by pipeline v{version}, which predates "
            f"v{_MIN_RECOGNIZED_BRIEF_VERSION}'s evidence-type fields. "
            f"Rendering may use 'currently held' language for historical-"
            f"receipt cases. Re-emit the brief for accurate framing."
        )
    return None


