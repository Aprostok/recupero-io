# ---- Stolen-asset issuer resolution (v0.20.1) ---- #


def _resolve_theft_asset_issuer_name(
    theft_transfer: Transfer, *, fallback: str,
) -> str:
    """Resolve the actual issuer of the stolen token via the issuer DB.

    Pre-v0.20.1 the `asset.issuer` field in the freeze-letter context
    was set to the freeze-TARGET issuer (e.g., Tether for a downstream
    USDT freeze), which meant a freeze letter sent to Tether about
    USDT recoverable positions read "Asset issuer: Tether" in Section 2
    even when the originally-stolen token was msyrupUSDp issued by
    Midas. The two facts (what was stolen vs whom we're asking to
    freeze) are distinct and both belong on the letter.

    Falls back to ``fallback`` if no matching entry — preserves
    backward compatibility for cases where the stolen token isn't in
    the issuer DB.
    """
    try:
        from recupero._common import canonical_address_key as _ck
        from recupero.freeze.asks import load_issuer_db
        contract = theft_transfer.token.contract
        if not contract:
            return fallback
        db = load_issuer_db()
        canon = _ck(contract)
        entry = db.get((theft_transfer.chain, canon))
        if entry and entry.issuer:
            return entry.issuer
    except Exception as exc:  # noqa: BLE001
        log.warning(
            "could not resolve stolen-asset issuer for %s: %s — falling "
            "back to %s", theft_transfer.token.symbol, exc, fallback,
        )
    return fallback


# ---- Small generate_briefs helpers (v0.20.0 Phase C decomposition) ---- #


def _resolve_render_time() -> datetime:
    """Resolve the wall-clock-or-pinned render timestamp.

    Honors ``SOURCE_DATE_EPOCH`` for reproducible-builds workflows.
    On parse failure, falls back to ``datetime.now(UTC)``. v0.20.0
    Phase C extraction: previously inlined ~15 lines inside
    generate_briefs() with two ad-hoc imports; clarified into a
    single pure helper.
    """
    import os
    src_epoch = os.environ.get("SOURCE_DATE_EPOCH", "").strip()
    if src_epoch:
        try:
            return datetime.fromtimestamp(int(src_epoch), tz=UTC)
        except (ValueError, TypeError):
            log.warning(
                "SOURCE_DATE_EPOCH=%r is not a valid integer epoch; "
                "falling back to wall-clock", src_epoch,
            )
    return datetime.now(UTC)


def _make_brief_id(case_id: str, theft_tx_hash: str) -> str:
    """Deterministic brief identifier.

    v0.16.7 (round-9 output-artifacts CRIT): pre-v0.16.7 brief_id was
    `f"BRIEF-{wall_clock}-{uuid4().hex[:6]}"`, which made PDFs and
    HTML artifacts non-reproducible — re-running the same case
    produced different bytes (filename, cover-page text, footer code).
    Chain-of-custody hash verification was therefore impossible.

    The fix: derive brief_id deterministically from case content
    (case_id + earliest theft_transfer hash). Two renders of the same
    case → same brief_id → byte-reproducible artifacts.

    v0.20.0 Phase C extraction: moved out of generate_briefs() body
    into a pure helper so the rationale lives next to the algorithm
    instead of as a 17-line inline comment block in an orchestrator.
    """
    import hashlib
    seed = f"{case_id}|{theft_tx_hash}"
    suffix = hashlib.sha256(seed.encode("utf-8")).hexdigest()[:6]
    return f"BRIEF-{case_id[:8]}-{suffix}"


def generate_briefs(
    *,
    primary_case: Case,
    linked_cases: list[Case],
    victim: VictimInfo,
    investigator: InvestigatorInfo,
    case_dir: Path,
    issuer: IssuerInfo = MIDAS_ISSUER,
    asset_type: str = "ERC-20 yield-bearing wrapper token",
    asset_usd_value_current: str | None = None,
    outbound_count_of_stolen_asset: int = 0,
    flow_filename: str | None = None,
    issuer_freezable: dict | None = None,
    # v0.18.6 (round-11 PDF-CRIT-006): IC3 case ID flows through
    # editorial JSON (cases.ic3_case_id captured at intake) but was
    # never wired into the LE handoff. Operators curate it specifically
    # so FBI can cross-reference the IC3 record; pre-v0.18.6 the
    # handoff dropped it entirely.
    ic3_case_id: str | None = None,
    draft: bool = False,
    draft_label: str | None = None,
) -> BriefBundle:
    """Render both briefs and write them to disk.

    ``issuer_freezable`` (added 2026-05-15 in response to the
    operator-quality review on case e917ffc5) is the per-issuer
    FREEZABLE entry from ``freeze_brief.json``. When provided, the
    template renders an issuer-specific "current location + ask"
    that lists each holding (status FREEZABLE or INVESTIGATE) the
    issuer actually controls, instead of generically asking every
    issuer to freeze the original ETH at the first hop.

    Shape (matches freeze_brief.json):

      {
        "issuer": "Circle",
        "token": "USDC",
        "total_usd": "$7,097.58",
        "total_suspected_usd": "$1,037,451.35",
        "freeze_capability": "HIGH",
        "holdings": [
          {"address": "0xABC...",
           "amount": "1066.27 USDC",
           "usd": "$1,066.27",
           "status": "FREEZABLE"},
          ...
        ],
        ...
      }

    When None (the legacy/wallet-trace path), the template falls
    back to single-asset/single-holder rendering using
    ``theft_transfer`` as the source. Backward-compatible for any
    caller that hasn't yet been updated to pass per-issuer data.
    """
    # Identify the theft event(s). _find_theft_events returns the full
    # drain cluster (primary event first, then any others within the
    # 7-day window). For real-world cases drained across multiple
    # transactions, this surfaces the full timeline in the LE handoff
    # while keeping the freeze letter focused on the primary event.
    theft_events = _find_theft_events(primary_case)
    if not theft_events:
        raise ValueError(
            f"No transfers in case {primary_case.case_id} could be identified as the theft event."
        )
    theft_transfer = theft_events[0]  # primary (headline) event

    # Build the forwarding chain across linked cases.
    # v0.17.2 (POLISH-3): also build the full fan-out tree so the LE
    # handoff template can render every dispersal branch, not just
    # the primary linear chain. The legacy `hops` field is kept for
    # back-compat with v0.16.x templates; new templates should consume
    # `hops_tree` for the complete picture.
    hops = _build_hops(theft_transfer, linked_cases)
    hops_tree = _build_hop_tree(theft_transfer, linked_cases)

    # v0.20.2 (audit-round-3 R3-12): track the per-hop chain on the
    # current_holder, not just the address. On a cross-chain trail
    # (ETH drain → bridge → Tron USDT), `hops[-1].chain` may differ
    # from `primary_case.chain`. Pre-v0.20.2 the
    # current_holder.explorer_url was always built from
    # primary_case.chain — so a Tron current-holder linked to
    # etherscan/404. Now we route the explorer URL through the
    # actual chain of the final hop.
    if hops:
        current_holder_addr = hops[-1].to_address
        current_holder_chain = hops[-1].chain
    else:
        current_holder_addr = theft_transfer.to_address
        current_holder_chain = theft_transfer.chain

    # Identify wallets across all cases for the LE summary table
    identified_wallets = _build_identified_wallets(primary_case, linked_cases, victim, current_holder_addr)

    # Common context. v0.20.0 Phase C: render-time + deterministic
    # brief-id resolution extracted to small pure helpers above. The
    # rationale + reproducible-builds contract live with the helpers
    # so the orchestrator can read as orchestration.
    now = _resolve_render_time()
    brief_id = _make_brief_id(primary_case.case_id, theft_transfer.tx_hash)

    ctx: dict[str, Any] = {
        "case_id": primary_case.case_id,
        "brief_id": brief_id,
        # v0.18.6: IC3 reference + DRAFT watermark plumbed into ctx.
        "ic3_case_id": ic3_case_id,
        "draft": draft,
        "draft_label": draft_label or "DRAFT",
        # v0.19.1 (round-12 PDF-CRIT-4): the 23 templates that render this
        # field already append a literal " UTC" suffix. Pre-v0.19.1 the
        # value carried a trailing 'Z', producing "2026-05-19T17:00:00Z UTC"
        # on every cover page + footer + section header — stuttered
        # timestamp on legal documents reads as templated/broken to LE
        # and lawyers. Strip the 'Z'; templates own the UTC marker.
        "generated_at": now.strftime("%Y-%m-%dT%H:%M:%S"),
        "verified_at": now.strftime("%Y-%m-%d"),
        # v0.17.4 (round-10 audit HIGH): primary_chain populated for the
        # LE handoff template. Pre-v0.17.4 the template's
        # `{{ primary_chain | default("Ethereum") }}` silently rendered
        # "Ethereum" for every Solana/Tron/BSC theft, putting a wrong-
        # chain attestation on the cover page of every non-ETH brief.
        # v0.18.6 (round-11 PDF-HIGH-010): use emit_brief's display-name
        # map so "bsc" → "BNB Chain" instead of `.capitalize()` =
        # "Bsc". Pre-v0.18.6 the LE handoff cover for a BSC case
        # printed "USDT on Bsc"; the trace report for the same case
        # printed "BNB Chain". Inconsistent + unprofessional.
        "primary_chain": _resolve_primary_chain_display(primary_case),
        # v0.20.2 (audit-round-3 R3-9/R3-10): canonical block-
        # explorer display name for the case chain, so letter prose
        # can render "View on Tronscan" on a Tron case instead of
        # the hard-coded "View on Etherscan" that was rendering
        # alongside Tronscan URLs. Centralized in _common; templates
        # use `{{ primary_chain_explorer_name }}` (with safe
        # defaults).
        "primary_chain_explorer_name": _common_explorer_name_for_chain(
            primary_case.chain,
        ),
        "trace_started_at": (
            primary_case.trace_started_at.strftime("%Y-%m-%dT%H:%M:%SZ")
            if primary_case.trace_started_at else None
        ),
        "software_version": __version__,
        "victim": victim.model_dump(),
        "investigator": investigator.__dict__,
        "asset": {
            "symbol": theft_transfer.token.symbol,
            "contract": theft_transfer.token.contract or "(native)",
            # v0.20.1 (Jacob V-CFI01 residual #6): "issuer" on the
            # asset block reflects the STOLEN-TOKEN's actual issuer
            # (resolved from the issuer DB via contract address), NOT
            # the freeze-target issuer. Pre-v0.20.1 these were
            # conflated: a Tether-targeted freeze letter for an
            # mSyrupUSDp theft labeled "Asset issuer: Tether" even
            # though mSyrupUSDp is issued by Midas. Section 2 of the
            # letter now reads "Asset issuer: Midas" while the rest
            # of the letter correctly targets Tether for the downstream
            # USDT freeze. Both facts coexist; neither overwrites the
            # other.
            "issuer": _resolve_theft_asset_issuer_name(
                theft_transfer, fallback=issuer.name,
            ),
            "issuer_short": issuer.short_name,
            "type": asset_type,
            "description": issuer.asset_description or asset_type,
            "amount_human": _fmt_decimal(theft_transfer.amount_decimal),
            "usd_value_at_theft": _fmt_usd(theft_transfer.usd_value_at_tx),
            # v0.20.2 (audit-round-3 R3-3): expose aggregate
            # across all theft_events so the headline asset block
            # renders the TOTAL stolen value on multi-event drains
            # (e.g., V-CFI01 shape: 6 × $600K = $3.6M). The
            # `usd_value_at_theft` field stays as the primary
            # event's USD (back-compat for callers that depend on
            # the single-event semantics); templates that want
            # the multi-event headline use `total_usd_value_at_theft`.
            "total_usd_value_at_theft": _fmt_usd(
                sum(
                    (t.usd_value_at_tx for t in theft_events
                     if t.usd_value_at_tx is not None),
                    start=Decimal(0),
                ) or theft_transfer.usd_value_at_tx,
            ),
            "total_amount_human": _fmt_decimal(
                sum(
                    (t.amount_decimal for t in theft_events
                     if t.amount_decimal is not None),
                    start=Decimal(0),
                ) or theft_transfer.amount_decimal,
            ),
            "theft_event_count": len(theft_events),
            "is_multi_event": len(theft_events) > 1,
            "usd_value_current": asset_usd_value_current,
        },
        "issuer": {
            "name": issuer.name,
            "short_name": issuer.short_name,
            "contact_email": issuer.contact_email,
            "jurisdiction": issuer.jurisdiction,
            "regulatory_framework": issuer.regulatory_framework,
            "secondary_party": issuer.secondary_party,
            "secondary_role": issuer.secondary_role,
            "kyc_required": issuer.kyc_required,
            "kyc_minimum": issuer.kyc_minimum,
        },
        # Address URLs are computed per-row so every raw address shown
        # in the letter is a click-through link in the rendered PDF.
        # WeasyPrint converts <a href=".."> annotations into PDF link
        # rectangles, so compliance reviewers can jump straight from
        # the document to the on-chain record.
        "theft_event": {
            "tx_hash": theft_transfer.tx_hash,
            "block_number": theft_transfer.block_number,
            "timestamp_human": theft_transfer.block_time.strftime("%Y-%m-%d %H:%M:%S"),
            "from_address": theft_transfer.from_address,
            "to_address": theft_transfer.to_address,
            "explorer_url": theft_transfer.explorer_url,
            "from_explorer_url": _address_explorer_url(
                theft_transfer.from_address, theft_transfer.chain
            ),
            "to_explorer_url": _address_explorer_url(
                theft_transfer.to_address, theft_transfer.chain
            ),
        },
        # Full theft-event cluster, sorted by block_time ascending for
        # chronological rendering in section 3 of the LE handoff.
        # The primary event is at index 0 unless block_time orders it
        # later; templates that want strictly chronological should sort.
        # ``theft_event_count`` and ``theft_event_total_usd`` are
        # convenience aggregates for prose generation.
        "theft_events": [
            {
                "tx_hash": t.tx_hash,
                "block_number": t.block_number,
                "timestamp_human": t.block_time.strftime("%Y-%m-%d %H:%M:%S"),
                "from_address": t.from_address,
                "to_address": t.to_address,
                "amount_human": _fmt_decimal(t.amount_decimal),
                "symbol": t.token.symbol,
                "usd_value": _fmt_usd(t.usd_value_at_tx),
                "explorer_url": t.explorer_url,
                "from_explorer_url": _address_explorer_url(t.from_address, t.chain),
                "to_explorer_url": _address_explorer_url(t.to_address, t.chain),
                "is_primary": t.transfer_id == theft_transfer.transfer_id,
            }
            for t in sorted(theft_events, key=lambda x: x.block_time)
        ],
        "theft_event_count": len(theft_events),
        "theft_event_total_usd": _fmt_usd(
            sum(
                (t.usd_value_at_tx for t in theft_events
                 if t.usd_value_at_tx is not None),
                start=Decimal(0),
            )
        ),
        "hops": [
            {
                "tx_hash": h.tx_hash,
                "timestamp_human": h.block_time.strftime("%Y-%m-%d %H:%M:%S"),
                "from_address": h.from_address,
                "to_address": h.to_address,
                "amount_human": _fmt_decimal(h.amount_decimal),
                "symbol": h.token.symbol,
                "explorer_url": h.explorer_url,
                "from_explorer_url": _address_explorer_url(h.from_address, h.chain),
                "to_explorer_url": _address_explorer_url(h.to_address, h.chain),
            }
            for h in hops
        ],
        # v0.17.2 (POLISH-3): full fan-out tree exposed for templates
        # that want to render every dispersal branch. Each entry carries
        # the same shape as `hops` entries plus `usd_value` for
        # USD-rank-aware sorting in the template.
        "hops_tree": [
            {
                "tx_hash": h.tx_hash,
                "timestamp_human": h.block_time.strftime("%Y-%m-%d %H:%M:%S"),
                "from_address": h.from_address,
                "to_address": h.to_address,
                "amount_human": _fmt_decimal(h.amount_decimal),
                "symbol": h.token.symbol,
                "usd_value": (
                    f"${h.usd_value_at_tx:,.2f}"
                    if h.usd_value_at_tx is not None else None
                ),
                "explorer_url": h.explorer_url,
                "from_explorer_url": _address_explorer_url(h.from_address, h.chain),
                "to_explorer_url": _address_explorer_url(h.to_address, h.chain),
            }
            for h in hops_tree
        ],
        "hops_tree_branch_count": len(hops_tree),
        "hops_tree_has_fanout": len(hops_tree) > len(hops),
        # v0.20.1 (Jacob V-CFI01 residual #3): "forwarding_observed"
        # is True whenever we have ANY evidence of downstream movement,
        # including fan-out branches OR per-issuer freezable holdings
        # (which represent received-from-perp downstream addresses).
        # Pre-v0.20.1 the freeze-letter template gated Section 3's "no
        # forwarding observed" stub solely on `hops` — for cases where
        # the linear hop chain is empty but the issuer freezable list
        # has downstream wallets, the stub printed contradicting
        # Section 4 (which listed those wallets).
        "forwarding_observed": bool(
            hops
            or len(hops_tree) > 0
            or (
                issuer_freezable
                and isinstance(issuer_freezable, dict)
                and issuer_freezable.get("holdings")
            )
        ),
        "current_holder": {
            "address": current_holder_addr,
            # v0.20.2 (audit-round-3 R3-12): per-hop chain so a
            # cross-chain current-holder (e.g., Tron USDT after a
            # bridge) links to the right block explorer instead of
            # always-etherscan.
            "explorer_url": _address_explorer_url(
                current_holder_addr, current_holder_chain,
            ),
        },
        # Asset contract address: clickable token-contract page when
        # the contract is known (None for native ETH / BTC).
        "asset_contract_explorer_url": (
            _address_explorer_url(theft_transfer.token.contract,
                                  theft_transfer.chain)
            if theft_transfer.token.contract else None
        ),
        # Victim wallet — first interaction many compliance reviewers
        # will want is to verify the seed wallet on chain.
        "victim_wallet_explorer_url": _address_explorer_url(
            victim.wallet_address, primary_case.chain
        ),
        "outbound_count_of_stolen_asset": outbound_count_of_stolen_asset,
        "identified_wallets": identified_wallets,
        # Fund-flow diagram lives in briefs/flow_<hash>.svg as a
        # standalone artifact. Letters reference it via attachment-
        # pointer in section 3 (no inline embed — the inline version
        # was unreadable when recipients re-printed the letter to
        # PDF). Templates guard with ``{% if flow_filename %}``.
        "flow_filename": flow_filename,
        # Per-issuer freezable holdings — see docstring above.
        # Pre-rendered for the template via ``_build_issuer_freezable_ctx``
        # so the template doesn't have to know about freeze_brief.json
        # internals or compute explorer URLs.
        "issuer_freezable": _build_issuer_freezable_ctx(
            issuer_freezable, primary_case.chain,
        ),
        # Law-enforcement filing-route recommendation. Only the LE
        # template uses this; the issuer freeze letter ignores the
        # ``le_routing`` context key. Built from victim's state +
        # country + (approximate) total loss so the LE handoff names
        # specific filing channels (IC3 + state AG + escalation tiers).
        #
        # v0.20.2 (audit-round-2 finding #4): LE-routing thresholds
        # (IC3-only / state-AG / multi-jurisdiction escalation) are
        # driven by TOTAL loss, not just the primary theft event.
        # Pre-v0.20.2 we passed `theft_transfer.usd_value_at_tx` —
        # in a V-CFI01-shape case the drain is split across N
        # transactions (e.g. 6 × $600K = $3.6M total), and using the
        # primary alone routed the case to the wrong tier and
        # understated severity in the LE handoff narrative.
        "le_routing": _build_le_routing_ctx(
            victim,
            sum(
                (t.usd_value_at_tx for t in theft_events
                 if t.usd_value_at_tx is not None),
                start=Decimal(0),
            ) or theft_transfer.usd_value_at_tx,
        ),
    }

