def _find_theft_transfer(case: Case) -> Transfer | None:
    """The primary theft event — the highest-USD transfer in the
    drain cluster. Backward-compat wrapper around
    ``_find_theft_events``; returns the first (largest-by-USD) item.

    Most letter sections render this single transfer as the headline
    "Theft Event". Section 3 of the LE handoff additionally renders
    the full cluster from ``_find_theft_events`` when the drain
    spans multiple transactions.
    """
    events = _find_theft_events(case)
    return events[0] if events else None


def _find_theft_events(
    case: Case,
    *,
    time_window_hours: int = 168,
) -> list[Transfer]:
    """Find every transfer that looks like part of the drain.

    Real victims are often drained across multiple transactions —
    e.g., a phishing attack that approves a token's spender contract
    and then drains $X in piece-meal trades over hours, or a wallet
    that's slowly emptied as the perpetrator avoids triggering
    exchange-side monitoring thresholds. Before this function, the
    brief rendered ONE event (the biggest), implying a single
    theft; the letter prose said "$X was stolen on day Y", missing
    the actual story of "$X was stolen across N transactions on
    days Y through Y+3".

    Algorithm
    ---------

    1. Identify the largest-USD transfer in the case (the primary
       theft event). Same logic as the legacy single-event picker.
    2. Cluster around it: include every other transfer with
       ``block_time`` within ``time_window_hours`` of the primary
       event's block_time. The default window (168 hours / 7 days)
       is conservative — most drain campaigns wrap in under a week.
    3. Return the cluster ordered by block_time ascending (so the
       template can render them chronologically).

    Notes
    -----
    * Only outbound transfers from the seed (victim) address count
      as theft events. Internal-to-internal transfers (e.g., the
      perpetrator moving funds around) are part of the
      ``hops`` chain, not the theft events.
    * Returns the primary event first in the returned list (which
      may not be chronologically first), so callers reading
      ``events[0]`` get the headline. The chronological ordering is
      via ``sorted(events, key=block_time)`` if needed for the
      timeline render.
    * Empty list if no transfers at all in the case (wallet trace
      that returned nothing).

    Returns
    -------
    A list ordered with the primary event first, then all other
    events in the cluster sorted by block_time ascending. Empty
    list if no transfers in the case.
    """
    if not case.transfers:
        return []

    from recupero._common import canonical_address_key as _ck
    seed_lower = _ck(case.seed_address)

    # Outbound transfers from the victim
    outbound = [
        t for t in case.transfers
        if _ck(t.from_address) == seed_lower
    ]
    if not outbound:
        # Fallback: any transfer in the case (preserves legacy
        # behavior for cases where the seed_address normalization
        # diverges or the case has no direct-from-seed transfers).
        outbound = list(case.transfers)

    # Primary event: highest-USD with sensible fallback.
    #
    # v0.18.0 (round-11 forensic-CRIT-005): pre-v0.18.0 the unpriced
    # fallback was `max(outbound, key=amount_decimal)` — which compares
    # raw token-amount values across different tokens. Result: a
    # 1,000,000,000,000-token shitcoin transfer was picked as "primary
    # theft event" over a 1 ETH transfer, because Decimal compares the
    # numeric value of amount_decimal regardless of what token it is.
    # The LE handoff cover then claimed the headline theft was a
    # billion-shitcoin transfer instead of the real $3K ETH theft.
    #
    # New behavior:
    #   1. Prefer priced transfers (still picks max USD).
    #   2. Unpriced fallback: prefer NATIVE-asset transfers first (ETH,
    #      BNB, SOL, etc.) since those are typically real currency; among
    #      those pick the largest. Then known stablecoins (USDC, USDT,
    #      DAI). Then everything else by recency (most-recent first).
    #   3. Final fallback: first by block_time ascending (oldest first).
    #
    # The result is operationally never wrong — if priced data exists
    # at all, the USD ranking dominates. The unpriced fallback is a
    # last-resort heuristic and we surface it via a `log.warning`.
    priced = [t for t in outbound if t.usd_value_at_tx is not None]
    if priced:
        primary = max(priced, key=lambda t: t.usd_value_at_tx)
    else:
        _NATIVE_SYMBOLS = {"ETH", "BNB", "SOL", "TRX", "BTC", "POL", "MATIC", "AVAX"}
        _STABLE_SYMBOLS = {"USDC", "USDT", "DAI", "BUSD", "FDUSD", "TUSD", "USDP", "USDE", "USDe", "PYUSD"}

        def _rank_key(t):
            sym = (t.token.symbol or "").upper()
            if sym in _NATIVE_SYMBOLS:
                tier = 0
            elif sym in _STABLE_SYMBOLS:
                tier = 1
            else:
                tier = 2
            # Within tier, prefer larger amount_decimal but the
            # comparison is now within a SINGLE token class (native,
            # stablecoin, or other). Negate so max() picks the largest.
            return (tier, -float(t.amount_decimal or 0), t.block_time)

        primary = min(outbound, key=_rank_key, default=None)
        if primary is not None:
            log.warning(
                "_find_theft_events: no transfer carried usd_value_at_tx; "
                "picked primary by token-class fallback (sym=%s, "
                "amount=%s, tx=%s). Pricing failure upstream — verify "
                "the brief's headline before LE handoff.",
                primary.token.symbol, primary.amount_decimal, primary.tx_hash,
            )
    if primary is None:
        return []

    # Cluster around the primary event's block_time
    from datetime import timedelta
    window = timedelta(hours=time_window_hours)
    primary_time = primary.block_time

    cluster = [
        t for t in outbound
        if abs(t.block_time - primary_time) <= window
        and t.transfer_id != primary.transfer_id
    ]

    # Sort cluster by block_time ascending; prepend primary so
    # callers reading [0] get the headline event consistently
    cluster.sort(key=lambda t: t.block_time)
    return [primary] + cluster


def _pluralize_filter(n: int, singular: str, plural: str | None = None) -> str:
    """Jinja filter: return singular or plural noun based on count.

    Usage in templates:
      {{ n }} {{ n | pluralize("transfer") }}        -> "1 transfer" / "2 transfers"
      {{ n }} {{ n | pluralize("hop", "hops") }}     -> "1 hop"      / "2 hops"
      {{ n }} {{ n | pluralize("address", "addresses") }}

    Auto-pluralization adds "s" when plural is None. Pass the explicit
    plural form for irregular nouns ("addresses", "criteria", etc.).
    v0.17.2 (output polish POLISH-4).
    """
    try:
        count = int(n)
    except (TypeError, ValueError):
        return singular
    if count == 1:
        return singular
    return plural if plural is not None else singular + "s"


_BASE58_CHAINS: frozenset[Chain] = frozenset({
    Chain.solana,
    Chain.tron,
    Chain.bitcoin,
})


def _hop_addr_key(chain: Chain, address: str) -> str:
    """Chain-aware key for the hop-builder visited set + index.

    EVM hex addresses are lowercased (case-insensitive). Base58 addresses
    (Solana/Tron/Bitcoin) preserve case — lowercasing them produced invalid
    keys and let `_build_hops` infinite-loop on a case mismatch in v0.16.6.
    Token contracts always lowercased (EVM-only namespace).
    """
    if chain in _BASE58_CHAINS:
        return address
    return address.lower()


def _build_hops(theft: Transfer, linked_cases: list[Case]) -> list[Transfer]:
    """Find subsequent transfers of the stolen asset across linked cases.

    Walks forward picking the EARLIEST forwarding tx at each step
    (the natural primary chain). Returns the primary linear path
    only; callers that want the full fan-out should use
    ``_build_hop_tree`` instead. This signature is kept for
    backward compatibility with existing template renders that
    read a flat `hops` list.
    """
    tree = _build_hop_tree(theft, linked_cases)
    return _flatten_primary_chain(tree)


def _build_hop_tree(
    theft: Transfer,
    linked_cases: list[Case],
    *,
    max_branches_per_node: int = 5,
    max_depth: int = 6,
) -> list[Transfer]:
    """Build the full fan-out tree of subsequent transfers.

    v0.16.11 (round-9 output-artifacts HIGH): pre-v0.16.11 the hop
    builder walked ONLY the earliest forwarding tx at each step,
    silently dropping all sibling forwards. Real perpetrator wallets
    routinely split funds to N downstream addresses (the dispersal
    pattern); rendering just one branch under-reports the forwarding
    fan-out and misses subpoena targets that the LE handoff should
    name.

    The tree is returned flat (depth-first traversal) so existing
    callers iterating `hops` still see all transfers, BUT each
    transfer carries enough information (via `hop_depth` +
    `parent_transfer_id`) for templates to render a tree if they
    want to.

    Bounds: `max_branches_per_node` (5) keeps the brief readable;
    `max_depth` (6) prevents pathological deep-chain exploration.
    Both are well above typical real-case fan-out.
    """
    flat: list[Transfer] = []
    target_chain = theft.token.chain
    target_token_contract = (theft.token.contract or "").lower()
    start_key = _hop_addr_key(target_chain, theft.to_address)

    # Build a quick index: {(from_addr_key, token_contract_lower) -> list[Transfer]}
    # from_addr_key uses chain-aware normalization (base58 preserves case).
    index: dict[tuple[str, str], list[Transfer]] = {}
    for c in linked_cases:
        for t in c.transfers:
            if t.token.chain != target_chain:
                continue
            key = (_hop_addr_key(target_chain, t.from_address),
                   (t.token.contract or "").lower())
            index.setdefault(key, []).append(t)

    # DFS with visited-set to prevent cycles. Each branch contributes
    # up to `max_branches_per_node` transfers per source address;
    # USD-ranked so the highest-value branches are kept on tight caps.
    visited: set[str] = {start_key}

    def _walk(addr_key: str, depth: int) -> None:
        if depth >= max_depth:
            return
        candidates = list(index.get((addr_key, target_token_contract), []))
        if not candidates:
            return
        # Rank by (USD desc, block_number asc) — biggest moves first,
        # ties broken by chronology. The "primary" branch a template
        # picks for the linear-chain summary is candidates[0] post-sort.
        candidates.sort(
            key=lambda t: (
                -(float(t.usd_value_at_tx or 0)),
                t.block_number,
            ),
        )
        for next_hop in candidates[:max_branches_per_node]:
            next_key = _hop_addr_key(target_chain, next_hop.to_address)
            if next_key in visited:
                # Cycle. Don't recurse but still surface the edge —
                # the brief should show that the chain re-converges.
                flat.append(next_hop)
                continue
            visited.add(next_key)
            flat.append(next_hop)
            _walk(next_key, depth + 1)

    _walk(start_key, 0)
    return flat


def _flatten_primary_chain(tree: list[Transfer]) -> list[Transfer]:
    """Pick the PRIMARY linear chain (earliest forwarding tx at each
    step) from the full DFS-flattened tree.

    Backward-compatible with the pre-v0.16.11 `_build_hops` return
    value: a single linear path. Templates that want the fan-out
    should use `_build_hop_tree` directly.

    The DFS in `_build_hop_tree` rank-sorts candidates by USD desc;
    the primary chain follows the BIGGEST move at each node. That's
    the headline-amount path, which matches what compliance teams
    want to see in section 2 of the freeze letter.
    """
    if not tree:
        return []
    # The primary chain is the first transfer at each new from-address
    # in DFS order. Walk the flat tree and keep the first hit per
    # source address.
    seen_sources: set[str] = set()
    primary: list[Transfer] = []
    for t in tree:
        from_l = (t.from_address or "").lower()
        if from_l in seen_sources:
            continue
        seen_sources.add(from_l)
        primary.append(t)
    return primary


def _build_identified_wallets(
    primary: Case,
    linked: list[Case],
    victim: VictimInfo,
    current_holder: str,
) -> list[dict[str, Any]]:
    """Collect every distinct address mentioned across all cases, with role + label."""
    seen: dict[str, dict[str, Any]] = {}

    # Each identified wallet carries an ``explorer_url`` so the
    # rendered table makes every address a click-through to its
    # on-chain page.
    #
    # v0.20.2 (audit-round-3 R3-5): per-row chain — pre-v0.20.2 we
    # used `primary.chain` for every row. On a cross-chain trace
    # (e.g., ETH drain → bridge → Tron USDT), every Tron / Solana
    # / Bitcoin address linked to ``etherscan.io/address/<base58>``
    # → 404. We now track the chain of the transfer where each
    # address was first seen and pass it through to
    # _address_explorer_url, so the dispatcher renders the right
    # block explorer for each row.
    primary_chain = primary.chain

    from recupero._common import canonical_address_key as _ck

    def _add(
        addr: str, role: str, type_str: str, notes: str,
        row_class: str = "", chain: Any = None,
    ):
        # v0.20.2 (audit-round-2 finding #7): canonical-key dedup
        # so base58 chains (Solana / Tron / Bitcoin) aren't silently
        # corrupted by `.lower()`. EVM addresses still collapse case
        # variants; base58 case is preserved (it's significant
        # on-chain). Aligns this routine with the rest of the
        # codebase post-v0.17.5.
        key = _ck(addr)
        if key in seen:
            # Don't downgrade — first observation wins for role/notes
            return
        row_chain = chain if chain is not None else primary_chain
        seen[key] = {
            "address": addr, "role": role, "type": type_str,
            "notes": notes, "row_class": row_class,
            "explorer_url": _address_explorer_url(addr, row_chain),
        }

    _add(
        victim.wallet_address, "Victim", "EOA",
        "Source of stolen funds", "victim-row",
        chain=primary_chain,  # victim wallet always on primary case chain
    )

    victim_key = _ck(victim.wallet_address)
    current_holder_key = _ck(current_holder)
    for c in [primary, *linked]:
        for t in c.transfers:
            if _ck(t.from_address) == victim_key:
                continue  # victim already added
            cp_label = t.counterparty.label
            if _ck(t.to_address) == current_holder_key:
                role = "Current holder of stolen position"
                row_class = "perp-row"
            elif cp_label and cp_label.category.value == "perpetrator":
                role = f"Perpetrator wallet ({cp_label.name})"
                row_class = "perp-row"
            elif cp_label:
                role = cp_label.name
                row_class = ""
            else:
                role = "Unlabeled (under investigation)"
                row_class = ""
            type_str = "Contract" if t.counterparty.is_contract else "EOA"
            notes_parts = []
            if cp_label:
                notes_parts.append(f"Category: {cp_label.category.value}")
                if cp_label.notes:
                    notes_parts.append(cp_label.notes)
            notes = " · ".join(notes_parts) or "—"
            _add(t.to_address, role, type_str, notes, row_class, chain=t.chain)

    # Sort: victim, perpetrators, then everyone else
    def _sort_key(w: dict[str, Any]) -> tuple[int, str]:
        if "victim" in w["row_class"]: return (0, w["address"])
        if "perp" in w["row_class"]: return (1, w["address"])
        return (2, w["address"])

    return sorted(seen.values(), key=_sort_key)


def _fmt_decimal(d: Decimal | None) -> str:
    if d is None:
        return "—"
    if d == d.to_integral_value():
        return f"{int(d):,}"
    return f"{d:,.6f}".rstrip("0").rstrip(".")


